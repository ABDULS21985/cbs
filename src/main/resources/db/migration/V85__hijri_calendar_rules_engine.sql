CREATE TABLE IF NOT EXISTS notification_template_locale (
    id             BIGSERIAL PRIMARY KEY,
    template_id    BIGINT NOT NULL REFERENCES notification_template(id) ON DELETE CASCADE,
    locale         VARCHAR(20) NOT NULL,
    subject        VARCHAR(300),
    body           TEXT NOT NULL,
    body_html      TEXT,
    text_direction VARCHAR(10) NOT NULL DEFAULT 'AUTO' CHECK (text_direction IN ('LTR','RTL','AUTO')),
    font_family    VARCHAR(120),
    is_default     BOOLEAN NOT NULL DEFAULT FALSE,
    status         VARCHAR(20) NOT NULL DEFAULT 'DRAFT' CHECK (status IN ('DRAFT','ACTIVE','RETIRED')),
    reviewed_by    VARCHAR(100),
    reviewed_at    TIMESTAMP WITH TIME ZONE,
    tenant_id      BIGINT,
    created_at     TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
    updated_at     TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
    created_by     VARCHAR(100),
    updated_by     VARCHAR(100),
    version        BIGINT NOT NULL DEFAULT 0
);

CREATE UNIQUE INDEX IF NOT EXISTS uk_notification_template_locale_tenant
    ON notification_template_locale (template_id, LOWER(locale), COALESCE(tenant_id, -1));
CREATE INDEX IF NOT EXISTS idx_notification_template_locale_template
    ON notification_template_locale (template_id, tenant_id, status);

