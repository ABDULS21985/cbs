-- --------------------------------------------------------------------------
-- Hijri calendar engine
-- --------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS hijri_date_mapping (
    id                  BIGSERIAL PRIMARY KEY,
    hijri_year          INT NOT NULL,
    hijri_month         INT NOT NULL CHECK (hijri_month BETWEEN 1 AND 12),
    hijri_day           INT NOT NULL CHECK (hijri_day BETWEEN 1 AND 30),
    gregorian_date      DATE NOT NULL,
    hijri_month_name    VARCHAR(40) NOT NULL,
    hijri_month_name_ar VARCHAR(40) NOT NULL,
    day_of_week         VARCHAR(15) NOT NULL,
    source              VARCHAR(30) NOT NULL,
    tenant_id           BIGINT,
    created_at          TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
    updated_at          TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
    created_by          VARCHAR(100),
    updated_by          VARCHAR(100),
    version             BIGINT NOT NULL DEFAULT 0
);

CREATE UNIQUE INDEX IF NOT EXISTS uk_hijri_date_mapping_triplet_tenant
    ON hijri_date_mapping (hijri_year, hijri_month, hijri_day, COALESCE(tenant_id, -1));
CREATE UNIQUE INDEX IF NOT EXISTS uk_hijri_date_mapping_gregorian_tenant
    ON hijri_date_mapping (gregorian_date, COALESCE(tenant_id, -1));
CREATE INDEX IF NOT EXISTS idx_hijri_date_mapping_gregorian_date
    ON hijri_date_mapping (gregorian_date);
CREATE INDEX IF NOT EXISTS idx_hijri_date_mapping_year_month_tenant
    ON hijri_date_mapping (hijri_year, hijri_month, tenant_id);

CREATE TABLE IF NOT EXISTS hijri_holiday (
    id                  BIGSERIAL PRIMARY KEY,
    name                VARCHAR(120) NOT NULL,
    name_ar             VARCHAR(120),
    holiday_type        VARCHAR(30) NOT NULL CHECK (holiday_type IN ('PUBLIC_HOLIDAY','BANK_HOLIDAY','OBSERVANCE','RESTRICTED_TRADING')),
    hijri_month         INT NOT NULL CHECK (hijri_month BETWEEN 1 AND 12),
    hijri_day_from      INT NOT NULL CHECK (hijri_day_from BETWEEN 1 AND 30),
    hijri_day_to        INT NOT NULL CHECK (hijri_day_to BETWEEN 1 AND 30),
    duration_days       INT NOT NULL CHECK (duration_days >= 1),
    holiday_year        INT,
    affects_settlement  BOOLEAN NOT NULL DEFAULT FALSE,
    affects_trading     BOOLEAN NOT NULL DEFAULT FALSE,
    affects_profit      BOOLEAN NOT NULL DEFAULT FALSE,
    tenant_id           BIGINT,
    status              VARCHAR(20) NOT NULL DEFAULT 'ACTIVE' CHECK (status IN ('ACTIVE','INACTIVE')),
    notes               VARCHAR(500),
    created_at          TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
    updated_at          TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
    created_by          VARCHAR(100),
    updated_by          VARCHAR(100),
    version             BIGINT NOT NULL DEFAULT 0
);

CREATE INDEX IF NOT EXISTS idx_hijri_holiday_month_status_tenant
    ON hijri_holiday (hijri_month, status, tenant_id);
CREATE INDEX IF NOT EXISTS idx_hijri_holiday_tenant_status
    ON hijri_holiday (tenant_id, status);

-- --------------------------------------------------------------------------
-- Business rules engine
-- --------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS business_rule (
    id                       BIGSERIAL PRIMARY KEY,
    rule_code                VARCHAR(100) NOT NULL,
    name                     VARCHAR(500) NOT NULL,
    name_ar                  VARCHAR(500),
    description              TEXT,
    description_ar           TEXT,
    category                 VARCHAR(40) NOT NULL CHECK (category IN ('SHARIAH_COMPLIANCE','PRODUCT_GOVERNANCE','RISK_MANAGEMENT','REGULATORY','OPERATIONAL','PRICING','ELIGIBILITY','ACCOUNTING')),
    sub_category             VARCHAR(120),
    rule_type                VARCHAR(20) NOT NULL CHECK (rule_type IN ('CONSTRAINT','VALIDATION','CALCULATION','ROUTING','ELIGIBILITY','THRESHOLD','WORKFLOW')),
    severity                 VARCHAR(20) NOT NULL CHECK (severity IN ('BLOCKING','WARNING','INFORMATIONAL')),
    evaluation_expression    TEXT,
    parameters               JSONB NOT NULL DEFAULT '{}'::jsonb,
    error_message            VARCHAR(500),
    error_message_ar         VARCHAR(500),
    applicable_products      JSONB NOT NULL DEFAULT '[]'::jsonb,
    applicable_modules       JSONB NOT NULL DEFAULT '[]'::jsonb,
    effective_from           DATE NOT NULL,
    effective_to             DATE,
    status                   VARCHAR(20) NOT NULL DEFAULT 'DRAFT' CHECK (status IN ('DRAFT','ACTIVE','SUSPENDED','RETIRED')),
    priority                 INT NOT NULL DEFAULT 100,
    shariah_board_resolution VARCHAR(150),
    approved_by              VARCHAR(100),
    approved_at              TIMESTAMP WITH TIME ZONE,
    tenant_id                BIGINT,
    created_at               TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
    updated_at               TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
    created_by               VARCHAR(100),
    updated_by               VARCHAR(100),
    version                  BIGINT NOT NULL DEFAULT 0
);

CREATE UNIQUE INDEX IF NOT EXISTS uk_business_rule_code_tenant
    ON business_rule (LOWER(rule_code), COALESCE(tenant_id, -1));
CREATE INDEX IF NOT EXISTS idx_business_rule_status_tenant
    ON business_rule (status, tenant_id);
CREATE INDEX IF NOT EXISTS idx_business_rule_category_tenant
    ON business_rule (category, tenant_id);
CREATE INDEX IF NOT EXISTS idx_business_rule_effective
    ON business_rule (effective_from, effective_to);

CREATE TABLE IF NOT EXISTS business_rule_version (
    id                      BIGSERIAL PRIMARY KEY,
    rule_id                 BIGINT NOT NULL REFERENCES business_rule(id) ON DELETE CASCADE,
    version_number          INT NOT NULL,
    rule_snapshot           JSONB NOT NULL,
    decision_table_snapshot JSONB,
    change_description      VARCHAR(500) NOT NULL,
    change_type             VARCHAR(20) NOT NULL CHECK (change_type IN ('CREATED','MODIFIED','ACTIVATED','SUSPENDED','RETIRED')),
    changed_by              VARCHAR(100) NOT NULL,
    approved_by             VARCHAR(100),
    approval_reference      VARCHAR(150),
    effective_from          TIMESTAMP WITH TIME ZONE NOT NULL,
    effective_to            TIMESTAMP WITH TIME ZONE,
    tenant_id               BIGINT,
    created_at              TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
    updated_at              TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
    created_by              VARCHAR(100),
    updated_by              VARCHAR(100),
    version                 BIGINT NOT NULL DEFAULT 0,
    UNIQUE(rule_id, version_number)
);

CREATE INDEX IF NOT EXISTS idx_business_rule_version_rule
    ON business_rule_version (rule_id, version_number DESC);
CREATE INDEX IF NOT EXISTS idx_business_rule_version_effective
    ON business_rule_version (rule_id, effective_from, effective_to);

CREATE TABLE IF NOT EXISTS decision_table (
    id            BIGSERIAL PRIMARY KEY,
    rule_id       BIGINT NOT NULL REFERENCES business_rule(id) ON DELETE CASCADE,
    table_name    VARCHAR(200) NOT NULL,
    description   TEXT,
    input_columns JSONB NOT NULL DEFAULT '[]'::jsonb,
    output_columns JSONB NOT NULL DEFAULT '[]'::jsonb,
    hit_policy    VARCHAR(20) NOT NULL CHECK (hit_policy IN ('FIRST_MATCH','ALL_MATCHES','PRIORITY','COLLECT_SUM','COLLECT_MIN','COLLECT_MAX')),
    status        VARCHAR(20) NOT NULL DEFAULT 'DRAFT' CHECK (status IN ('DRAFT','ACTIVE','SUSPENDED','RETIRED')),
    table_version INT NOT NULL DEFAULT 1,
    tenant_id     BIGINT,
    created_at    TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
    updated_at    TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
    created_by    VARCHAR(100),
    updated_by    VARCHAR(100),
    version       BIGINT NOT NULL DEFAULT 0,
    UNIQUE(rule_id, table_name)
);

CREATE INDEX IF NOT EXISTS idx_decision_table_rule_status
    ON decision_table (rule_id, status);
CREATE INDEX IF NOT EXISTS idx_decision_table_status_tenant
    ON decision_table (status, tenant_id);

CREATE TABLE IF NOT EXISTS decision_table_row (
    id                BIGSERIAL PRIMARY KEY,
    decision_table_id BIGINT NOT NULL REFERENCES decision_table(id) ON DELETE CASCADE,
    row_number        INT NOT NULL,
    input_values      JSONB NOT NULL DEFAULT '[]'::jsonb,
    output_values     JSONB NOT NULL DEFAULT '[]'::jsonb,
    description       TEXT,
    is_active         BOOLEAN NOT NULL DEFAULT TRUE,
    priority          INT NOT NULL,
    created_at        TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
    updated_at        TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
    created_by        VARCHAR(100),
    updated_by        VARCHAR(100),
    version           BIGINT NOT NULL DEFAULT 0,
    UNIQUE(decision_table_id, row_number)
);

CREATE INDEX IF NOT EXISTS idx_decision_table_row_table
    ON decision_table_row (decision_table_id, row_number);
CREATE INDEX IF NOT EXISTS idx_decision_table_row_active
    ON decision_table_row (decision_table_id, is_active);

